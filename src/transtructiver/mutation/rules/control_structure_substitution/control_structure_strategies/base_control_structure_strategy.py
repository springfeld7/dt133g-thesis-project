"""
Base abstraction for language-specific control structure substitution strategies.

Each concrete strategy must define how to:
1. Identify valid control structure nodes
2. Transform them into equivalent structures
"""

from abc import ABC, abstractmethod
from typing import List

from .....node import Node
from ....mutation_context import MutationContext
from ...mutation_rule import MutationRecord, MutationRule


class BaseControlStructureStrategy(ABC):
    """
    Abstract base class for control structure substitution strategies.

    Concrete implementations must provide logic for identifying valid
    control structures and rewriting them into equivalent forms.

    Methods:
        is_valid(node): Determines if the node is a valid control structure candidate.
        apply(node, context, rule): Performs the transformation.
    """

    def _insert_node(self, context, parent, point, type, text, index, rule):
        """
        Creates and inserts a Node, then returns the generated insertion record.

        Args:
            context: The execution context providing unique IDs.
            parent: The parent Node receiving the new child.
            point: The location where the new node will be inserted.
            type (str): The node type for the Node and the record.
            text (str): The text content for the Node and the record.
            index (int): The position to insert the child in the parent's list.
            records (list): The list where the record is stored.
            rule: The rule object used to generate the record.

        Returns:
            dict: The record object generated by rule.record_insert.
        """
        new_node = Node(
            start_point=(context.next_id(), -1),
            end_point=point,
            type=type,
            text=text,
        )
        new_node.parent = parent
        parent.children.insert(index, new_node)

        record = rule.record_insert(
            point=new_node.start_point,
            insertion_point=point,
            new_text=new_node.text,
            new_type=type,
        )

        return record

    def _insert_segments(self, context, parent, point, segments, index, rule):
        """
        Inserts a batch of nodes and returns the resulting records.

        Args:
            context: The execution context providing unique IDs.
            parent: The parent Node receiving the new children.
            point: The location where the new nodes will be inserted.
            segments (list[tuple]): A list of (type, text) tuples to insert.
            index (int): The starting position for insertions.
            rule: The rule object used to generate records.

        Returns:
            list: A list of record objects generated during insertion.
        """
        records = []
        for node_type, content in segments:
            records.append(
                self._insert_node(context, parent, point, node_type, content, index, rule)
            )

        return records

    def _substitute(
        self, node: Node, new_type: str, new_text: str, rule: MutationRule
    ) -> MutationRecord:
        """
        Mutates node type/text and returns the substitution record.

        Args:
            node: The node to mutate.
            new_type: The new type to assign to the node.
            new_text: The new text to assign to the node.
            rule: The mutation rule instance used for recording the substitution.

        Returns:
            The generated mutation record.
        """
        old_type = node.type
        node.type = new_type
        node.text = new_text
        return rule.record_substitute(node, old_type)

    @abstractmethod
    def is_valid(self, node: Node) -> bool:
        """
        Determines whether a node is a valid candidate for transformation.

        Args:
            node (Node): The CST node.

        Returns:
            bool: True if the node can be transformed, False otherwise.
        """
        pass

    @abstractmethod
    def apply(
        self, node: Node, rule: MutationRule, context: MutationContext, indent_unit: str, level: int
    ) -> List[MutationRecord]:
        """
        Transforms a valid control structure into an equivalent form.

        Args:
            node (Node): The CST node representing the control structure.
            context (MutationContext): Context for mutation tracking.
            indent_unit (str): The indentation unit for the language.
            level (int): The transformation level to apply.

        Returns:
            List[MutationRecord]: Records describing the transformation.
        """
        pass

    def _get_indent(self, node: Node) -> str:
        """
        Retrieves the indentation whitespace for a given node by examining its preceding siblings.

        Assumes indenation is represented by a whitespace node immediately preceding the target node.

        Args:
            node (Node): The node for which to find indentation.

        Returns:
            str: The indentation whitespace, or an empty string if none found.
        """
        parent = node.parent
        if not parent:
            return ""

        idx = parent.children.index(node)
        if idx == 0:
            return ""

        prev = parent.children[idx - 1]
        if prev.type != "whitespace":
            return ""

        return prev.text or ""
