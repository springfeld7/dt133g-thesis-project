"""C++-specific implementation of the dead code generation lexicon.

This module provides the concrete CppLexicon class, which translates
universal raw data into syntactically valid C++ code, including
assignment statements, identity modifications, opaque predicates, and unreachable loop headers.
"""

import random
from typing import Any
from .dead_code_lexicon import DeadCodeLexicon


class CppLexicon(DeadCodeLexicon):
    """
    Concrete lexicon for generating C++ dead code.

    Maintains internal state during a code generation transaction to ensure
    that identity operations (modifications) are compatible with the
    variable's assigned data type.
    """

    # Subclass-specific population of base-class lists
    OPAQUE_PREDICATES = [
        "false",
        "0 == 1",
        "true && false",
        "1 > 2",
        "!(true)",
        "0 != 0",
        "nullptr != nullptr",
        "sizeof(int) < 0",
        "static_cast<bool>(0)",
    ]

    UNREACHABLE_LOOP_HEADERS = [
        "while(false)",
        "while(0 == 1)",
        "while(1 > 2)",
        "for(int {var} = 0; {var} < 0; ++{var})",
        "for(int {var} = 0; {var} < 0; {var}+=1)",
        "for(int {var} = 10; {var} < 0; ++{var})",
        "for(std::string {var} : std::vector<std::string>{})",
    ]

    IDENTITY_OPS_STR = ['{var} += "";', "{var} = std::string({var});", "{var} = {var}.substr(0);"]

    IDENTITY_OPS_NUMERIC = ["{var} += 0;", "{var} *= 1;", "{var} -= 0;"]

    def __init__(self, rng: random.Random):
        """
        Initializes the CppLexicon.

        Args:
            rng (random.Random): A seeded random instance for deterministic generation.
        """
        super().__init__(rng)

    def get_assignment_statement(self, var_name: str, value: Any) -> str:
        """
        Constructs a C++ assignment statement.

        Args:
            var_name (str): The name of the variable to assign.
            value (Any): The raw value generated by generate_random_value.

        Returns:
            str: A formatted C++ assignment (e.g., "int var = 10;").
        """
        if self._current_type == "string":
            return f'std::string {var_name} = "{value}";'
        elif self._current_type == "int":
            return f"int {var_name} = {value};"
        elif self._current_type == "float":
            return f"double {var_name} = {value};"
        else:
            raise RuntimeError(f"Unsupported type for assignment: {self._current_type}")

    def format_block(self, header: str, body: str, prefix: str, is_if: bool) -> str:
        """
        Wraps code in a C++ block (if or loop) with correct brace syntax.

        Args:
            header (str): The condition or loop expression.
            body (str): The indented internal statements.
            prefix (str): The indentation for the block header.
            is_if (bool): Whether to format as an 'if' block.

        Returns:
            str: The fully constructed C++ code block.
        """
        block = f"{prefix}{'if ' + header if is_if else header} {{\n"
        block += body
        block += f"\n{prefix}}}"
        return block
