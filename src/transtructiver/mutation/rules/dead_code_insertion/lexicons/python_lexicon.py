"""Python-specific implementation of the dead code generation lexicon.

This module provides the concrete PythonLexicon class, which translates
universal raw data into syntactically valid Python code, including
assignment statements, identity modifications, opaque predicates, and unreachable loop headers.
"""

import random
from typing import Any
from .dead_code_lexicon import DeadCodeLexicon


class PythonLexicon(DeadCodeLexicon):
    """
    Concrete lexicon for generating Python dead code.

    Maintains internal state during a code generation transaction to ensure
    that identity operations (modifications) are compatible with the
    variable's assigned data type.
    """

    # Subclass-specific population of base-class lists
    OPAQUE_PREDICATES = [
        "False",
        "0 == 1",
        "bool(0)",
        "len('') > 0",
        "1 > 2",
        "not True",
        "False and True",
        "all([])",
        "any([])",
    ]

    UNREACHABLE_LOOP_HEADERS = [
        "while False",
        "while 0",
        "while 1 == 2",
        "for {var} in []",
        "for {var} in ()",
        "for {var} in set()",
        "for {var} in range(0)",
        "for {var} in {}.values()",
    ]

    IDENTITY_OPS_STR = [
        "{var} = {var} + ''",
        "{var} = str({var})",
        "{var} = {var}[:]",
    ]

    IDENTITY_OPS_NUMERIC = [
        "{var} += 0",
        "{var} *= 1",
        "{var} -= 0",
    ]

    def __init__(self, rng: random.Random):
        """
        Initializes the PythonLexicon.

        Args:
            rng (random.Random): A seeded random instance for deterministic generation.
        """
        super().__init__(rng)

    def get_assignment_statement(self, var_name: str, value: Any) -> str:
        """
        Constructs a Python assignment statement. Ensures strings are properly quoted.

        Args:
            var_name (str): The name of the variable to assign.
            value (Any): The raw value generated by generate_random_value.

        Returns:
            str: A formatted Python assignment (e.g., "var = 10").
        """
        # Ensure strings are quoted in the source code string
        val_repr = repr(value) if isinstance(value, str) else value
        return f"{var_name} = {val_repr}"

    def format_block(self, header: str, body: str, prefix: str, is_if: bool) -> str:
        """
        Wraps code in a Python block (if or loop) with correct colon syntax.

        Args:
            header (str): The condition or loop expression.
            body (str): The indented internal statements.
            prefix (str): The indentation for the block header.
            is_if (bool): Whether to format as an 'if' block.

        Returns:
            str: The fully constructed Python code block.
        """
        if is_if:
            # Add 'if ' prefix if the opaque predicate doesn't include it
            stmt_header = f"if {header}" if not header.startswith("if ") else header
        else:
            stmt_header = header

        return f"{prefix}{stmt_header}:\n{body}"
